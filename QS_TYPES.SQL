CREATE OR REPLACE TYPE T_QS_COL AS OBJECT (
		COLUMN_NAME VARCHAR2(100),
		COLUMN_TYPE	VARCHAR2(50),
		COLUMN_DIRECTIVES VARCHAR2(100)
	);
/
CREATE OR REPLACE TYPE T_QS_COLS IS TABLE OF T_QS_COL;
/
CREATE OR REPLACE TYPE T_QS_TABLE IS OBJECT (
		TABLE_NAME VARCHAR2(100),
		TABLE_DIRECTIVES VARCHAR2(100),
		TABLE_COLS T_QS_COLS
	);
/
CREATE OR REPLACE TYPE T_QS_TABLES IS TABLE OF T_QS_TABLE;
/